#include <iostream>
#include "funcs.h"

using namespace std;

void soma(float a,float b){
  cout<<endl<<"O valor da soma: "<<a<<" + "<<b<<" = "<<a+b<<endl;
}

void mult(float a,float b){
  cout<<endl<<"O valor da multiplicação: "<<a<<" * "<<b<<" = "<<a*b<<endl;
}

void divi(float a,float b){
  cout<<endl<<"O valor da divisão: "<<a<<" / "<<b<<" = "<<a/b<<endl;
}
void subt(float a,float b){
  cout<<endl<<"O valor da subtração: "<<a<<" - "<<b<<" = "<<a-b<<endl;
}
